        }

        public double BerekenOppervlakte(double a, double b, double c)
        {
            double s = (a + b + c) / 2;
            double area = Math.Sqrt(s * (s - a) * (s - b) * (s - c));
            return area;
        }

        private void berekenButton_Click(object sender, RoutedEventArgs e)
        {
            oppLabel.Content = BerekenOppervlakte(5, 5, 5);
        }
    }
